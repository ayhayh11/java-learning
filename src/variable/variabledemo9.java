package variable;

public class variabledemo9 {
    public static void main(String[] args) {
        //算数运算符：+ - * / %
        //整数计算
        int a = 10;
        int b = 3;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        //小数计算
        //小数直接运算又可能不精确,需要用到bigdecimal类.
        double c = 10.5;
        double d = 3.58;
        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);

    }
}
